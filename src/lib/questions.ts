export function levelToNum(level: string): number {
  if (level === "1.5") return 1.5;
  if (level === "2.5") return 2.5;
  if (level === "3.0") return 3.0;
  if (level === "3.5") return 3.5;
  if (level === "4.0") return 4.0;
  if (level === "4.5") return 4.5;
  return 5.2; // 5.0+
}
